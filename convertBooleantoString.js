// Implement a function which convert the given boolean value into its string representation.

// Note: Only valid inputs will be given.


function booleanToString(b){
    //using a ternary operator that evaluates the condition to the left of the "?" if it's true then the middle statement is returned, if false then the last statement is returned
    return b ? "true" : "false" 
  }



